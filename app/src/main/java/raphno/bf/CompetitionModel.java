package raphno.bf;

public class CompetitionModel {
    private int id;
    private String name;
    private String code;
    private String type;
    private String emblem;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }

    public String getType() {
        return type;
    }

    public String getEmblem() {
        return emblem;
    }
}

