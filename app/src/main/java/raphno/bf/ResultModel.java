package raphno.bf;

public class ResultModel {
    private int count;
    private String competitions;
    private String first;
    private String last;
    private int played;

    public int getCount() {
        return count;
    }

    public String getCompetitions() {
        return competitions;
    }

    public String getFirst() {
        return first;
    }

    public String getLast() {
        return last;
    }

    public int getPlayed() {
        return played;
    }
}
