package raphno.bf;

public class EquipeModel {
    private int id;
    private String name;
    private String shortName;
    private String tla;
    private String crest;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getShortName() {
        return shortName;
    }

    public String getTla() {
        return tla;
    }

    public String getCrest() {
        return crest;
    }
}
